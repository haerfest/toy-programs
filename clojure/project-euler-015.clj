;;; Lattice paths
;;;
;;; A 2 x 2 grid:
;;;
;;; a - b - c       6 - 3 - 1
;;; |   |   |       |   |   |
;;; d - e - f  ==>  3 - 2 - 1
;;; |   |   |       |   |   |
;;; g - h - i       1 - 1 - 1
;;;
;;; A 4 x 4 grid:
;;;
;;; 70-35 -15 - 5 - 1
;;; |   |   |   |   |
;;; 35-20 -10 - 4 - 1
;;; |   |   |   |   |
;;; 15-10 - 6 - 3 - 1
;;; |   |   |   |   |
;;; 5 - 4 - 3 - 2 - 1
;;; |   |   |   |   |
;;; 1 - 1 - 1 - 1 - 1

(def answer
  (memoize
    (fn [h v]
      (if (some zero? [h v])
        1
        (+ (answer (dec h) v) (answer h (dec v)))))))
